def convert_speed(value: float, from_unit: str, to_unit: str) -> float:
    """
    –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ—Ç —Å–∫–æ—Ä–æ—Å—Ç—å –º–µ–∂–¥—É —Ä–∞–∑–ª–∏—á–Ω—ã–º–∏ –µ–¥–∏–Ω–∏—Ü–∞–º–∏ –∏–∑–º–µ—Ä–µ–Ω–∏—è.

    –ü–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º—ã–µ –µ–¥–∏–Ω–∏—Ü—ã –∏–∑–º–µ—Ä–µ–Ω–∏—è —Å–∫–æ—Ä–æ—Å—Ç–∏:
    - 'm/s'   ‚Äî –º–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É
    - 'km/h'  ‚Äî –∫–∏–ª–æ–º–µ—Ç—Ä—ã –≤ —á–∞—Å
    - 'mph'   ‚Äî –º–∏–ª–∏ –≤ —á–∞—Å (miles per hour)
    - 'knots' ‚Äî —É–∑–ª—ã (–º–æ—Ä—Å–∫–∏–µ –º–∏–ª–∏ –≤ —á–∞—Å)

    üìò –°–ø—Ä–∞–≤–∫–∞ –∏ —Ñ–æ—Ä–º—É–ª—ã: https://www.metric-conversions.org/speed-conversion.htm

    –ü—Ä–∏–º–µ—Ä:
    convert_speed(100, 'km/h', 'mph')  # –ü–µ—Ä–µ–≤–µ–¥—ë—Ç 100 –∫–º/—á –≤ –º–∏–ª–∏ –≤ —á–∞—Å

    :param value: –ß–∏—Å–ª–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Å–∫–æ—Ä–æ—Å—Ç–∏ (–Ω–∞–ø—Ä–∏–º–µ—Ä, 27.8)
    :param from_unit: –í –∫–∞–∫–æ–π –µ–¥–∏–Ω–∏—Ü–µ —Å–µ–π—á–∞—Å –∏–∑–º–µ—Ä—è–µ—Ç—Å—è —Å–∫–æ—Ä–æ—Å—Ç—å (–Ω–∞–ø—Ä–∏–º–µ—Ä, 'm/s')
    :param to_unit: –í –∫–∞–∫—É—é –µ–¥–∏–Ω–∏—Ü—É –Ω—É–∂–Ω–æ –ø–µ—Ä–µ–≤–µ—Å—Ç–∏ (–Ω–∞–ø—Ä–∏–º–µ—Ä, 'knots')
    :return: –°–∫–æ—Ä–æ—Å—Ç—å –≤ —Ü–µ–ª–µ–≤–æ–π –µ–¥–∏–Ω–∏—Ü–µ (float)
    """
    # –ü—Ä–∏–≤–µ–¥–µ–Ω–∏–µ –≤—Å–µ—Ö –∑–Ω–∞—á–µ–Ω–∏–π –∫ –º/—Å, –∑–∞—Ç–µ–º –ø–µ—Ä–µ–≤–æ–¥ –≤ —Ü–µ–ª–µ–≤—É—é –µ–¥–∏–Ω–∏—Ü—É
    conversions = {
        'm/s': lambda x: x,           # –ú–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É –æ—Å—Ç–∞—é—Ç—Å—è –Ω–µ–∏–∑–º–µ–Ω–Ω—ã–º–∏
        'km/h': lambda x: x / 3.6,    # –ö–∏–ª–æ–º–µ—Ç—Ä—ã –≤ —á–∞—Å –≤ –º–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É
        'mph': lambda x: x * 0.44704,# –ú–∏–ª–∏ –≤ —á–∞—Å –≤ –º–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É
        'knots': lambda x: x * 0.514444 # –ú–æ—Ä—Å–∫–∏–µ –º–∏–ª–∏ –≤ —á–∞—Å –≤ –º–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É
    }

    # –û–±—Ä–∞—Ç–Ω—ã–µ –ø–µ—Ä–µ–≤–æ–¥—ã –∏–∑ –º–µ—Ç—Ä–æ–≤ –≤ —Å–µ–∫—É–Ω–¥—É –≤ —Ü–µ–ª–µ–≤—ã–µ –µ–¥–∏–Ω–∏—Ü—ã
    reverse_conversions = {
        'm/s': lambda x: x,           # –ú–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É –æ—Å—Ç–∞—é—Ç—Å—è —Ç–∞–∫–∏–º–∏ –∂–µ
        'km/h': lambda x: x * 3.6,    # –ú–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É –≤ –∫–∏–ª–æ–º–µ—Ç—Ä—ã –≤ —á–∞—Å
        'mph': lambda x: x / 0.44704,# –ú–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É –≤ –º–∏–ª–∏ –≤ —á–∞—Å
        'knots': lambda x: x / 0.514444 # –ú–µ—Ç—Ä—ã –≤ —Å–µ–∫—É–Ω–¥—É –≤ –º–æ—Ä—Å–∫–∏–µ –º–∏–ª–∏ –≤ —á–∞—Å
    }

    # –°–Ω–∞—á–∞–ª–∞ –ø—Ä–∏–≤–æ–¥–∏–º –∑–Ω–∞—á–µ–Ω–∏–µ –∫ –º/—Å
    intermediate_value = conversions.get(from_unit)(value)

    # –ó–∞—Ç–µ–º –ø–µ—Ä–µ–≤–æ–¥–∏–º –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –≤ —Ü–µ–ª–µ–≤—É—é –µ–¥–∏–Ω–∏—Ü—É
    converted_value = reverse_conversions.get(to_unit)(intermediate_value)

    return round(converted_value, 4)  # –û–∫—Ä—É–≥–ª–∏–º –¥–æ —á–µ—Ç—ã—Ä—ë—Ö –∑–Ω–∞–∫–æ–≤ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π